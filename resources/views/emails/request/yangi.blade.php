<h3>"Raqamli texnologiyalar: sohalarda amaliy joriy etishning yechimlari va muammolari" mavzusidagi ilmiy-texnik anjumanida ishtirok eting</h3>

<p>
    2021 yil 28-29 aprel kunlari Muhammad al-Xorazmiy nomidagi Toshkent axborot texnologiyalari universiteti “Raqamli texnologiyalar: sohalarda amaliy joriy etishning yechimlari va muammolari” mavzuidagi Respublika ilmiy-texnik anjumanini o'tkazadi.<br>

    Anjuman quyidagi <b>yo'nalishlar</b> bo'yicha faoliyat yuritadi:<br>
    <ul>
        <li>Signallarga va tasvirlarga raqamli ishlov berish.</li>
        <li>Sun'iy intellekt.</li>
        <li>Tibbiyotda komp'yuter tizimlari va vositalari.</li>
        <li>Aqlli qishloq xo'jaligi uchun texnologiyalar va vositalar.</li>
        <li>Robototexnika.</li>
    </ul>

    Anjumanni o'tkazish uchun barcha sharoitlar tashkil etiladi: shaxsiy komp'yuter, elektron proektor va kerakli dasturiy ta'minot. Prezentatsiyalarni Power Point 97-2003 dasturida tayyorlash zarur.<br>

    Anjuman ma'ruzalarini 2021 yil 10 aprelgacha tegishli yunalish bo'yicha <a href="https://conferences-list.uz/conferences/2">conferences-list.uz</a> saytiga yuklash kerak.<br>

